#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	aux;

	if (nb < 0)
	{	
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			write(1, "2", 1);
			nb = -147483648;
		}	
		nb = -nb;
	}
	if (nb >= 0)
	{	
		if (nb / 10 > 0)
		{
			ft_putnbr(nb / 10);
		}
		aux = nb % 10 + '0';
		write(1, &aux, 1);
	}
}
